		public static void 직장인수강생출력(코스< ? super 직장인> course ) {
			System.out.println( course.get과정명() + " 수강생명단 : " + 
					Arrays.toString( course.get회원목록() ) );
		}
	
	
	
	
	
	
	
	
	
	
	
	
	
	
	
	
	
	
	
	
	
	
	
	
	
	
	
	
	
	

